package com.atcs.objects;

/**
 * @author davidtrower
 * This class holds values for the Plane
 */
public class Plane {

    private int id;
    private String type;
    private String size;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }
}
